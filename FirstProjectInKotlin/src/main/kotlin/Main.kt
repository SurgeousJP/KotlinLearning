fun main(args: Array<String>) {
    // Variables in Kotlin & String Format / Interpolation
    // You can define variables in two ways
    // Either using var <variableName>: <variableType> = <value>
    var userName: String = "SurgeousJP"
    // Or just assign to var and let it know itself
    var testUsername = "SurgeousJP"
    println("Hello $userName") // use $ sign to print variable value
    println("Hello $testUsername") //
    // Define Constant in Kotlin
    val pi = 3.14
    println("PI's value is $pi")
}
